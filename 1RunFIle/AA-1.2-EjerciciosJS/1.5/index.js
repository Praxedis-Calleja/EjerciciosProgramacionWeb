
const mayusculas = (palabra) => {
    return palabra.toUpperCase();
};

console.log(mayusculas('hola')); // 'HOLA'



// Explicación de cada parte:
// 1. `const sumar` - Declaramos una constante llamada `sumar` que almacenará nuestra función flecha.
// 2. `(a, b)` - Estos son los parámetros de la función, `a` y `b`.
// 3. `=>` - Esta es la sintaxis de la función flecha, que indica que estamos definiendo una función.
// 4. `{ return a + b; }` - Este es el cuerpo de la función, que en este caso retorna la suma de `a` y `b`.